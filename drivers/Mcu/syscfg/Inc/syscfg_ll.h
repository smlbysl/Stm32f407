static inline void SysCfg_LL_ConfEXTICR(const uint8_t port, const EXTI_LineType line)
{
    /* SYSCFG EXTICR config */
	SYSCFG_HW_SYSCFG->EXTICR[line / 4] &=~(0xFU << ((line % 4) * 4));
	SYSCFG_HW_SYSCFG->EXTICR[line / 4] |=(port << ((line % 4) * 4));
}

